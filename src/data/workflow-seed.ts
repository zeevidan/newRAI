export function tasksForProject(tasks: Task[], projectId: string) {
  return tasks.filter((task) => task.projectId === projectId)
}

export function messagesForProject(messages: Message[], projectId: string) {
  return messages.filter((message) => message.projectId === projectId)
}

export function activityForProject(activity: ActivityItem[], projectId: string) {
  return activity.filter((item) => item.projectId === projectId)
}

export function activityForAgent(activity: ActivityItem[], agentId: string) {
  return activity.filter((item) => item.agentId === agentId)
}

export function logsForAgent(logs: AgentLogEntry[], agentId: string) {
  return logs.filter((log) => log.agentId === agentId)
}
